#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*p;

	p = s;
	i = 0;
	if (!s)
		return ;
	while (i < n)
	{
		(*p) = 0;
		p++;
		i++;
	}
}
